function allA(s){return s.unidades.flatMap(u=>u.sesiones.flatMap(ss=>ss.actividades))}
function graded(acts){return acts.filter(a=>a.nota!==null&&a.estado==='asignada')}
function avg(arr){return arr.length?Math.round(arr.reduce((a,b)=>a+b,0)/arr.length):null}
function sAvg(s){return avg(graded(allA(s)).map(a=>a.nota))}
function uAvg(u){return avg(graded(u.sesiones.flatMap(ss=>ss.actividades)).map(a=>a.nota))}
function uComp(u){
  const a=u.sesiones.flatMap(ss=>ss.actividades).filter(a=>a.estado==='asignada');
  const d=a.filter(a=>a.nota!==null);
  return a.length?Math.round(d.length/a.length*100):0;
}
